def _build_open_positions_payload(db: Session, *, capital_tier: str) -> list[dict[str, Any]]:
    rows = _execute_mappings(
        db,
        """
        SELECT
            p.position_id,
            p.report_id,
            p.stock_code,
            p.capital_tier,
            p.position_status,
            p.signal_date,
            p.entry_date,
            p.actual_entry_price,
            p.exit_date,
            p.exit_price,
            p.shares,
            p.holding_days,
            p.net_return_pct,
            p.skip_reason,
            r.stock_name_snapshot,
            r.strategy_type
        FROM sim_position p
        LEFT JOIN report r ON r.report_id = p.report_id
        WHERE p.capital_tier = :capital_tier
          AND p.position_status = 'OPEN'
        ORDER BY p.created_at DESC, p.position_id DESC
        """,
        {"capital_tier": capital_tier},
    ).all()
    from datetime import date as _dt_date
    today_date = _dt_date.today()
    results = []
    for row in rows:
        # BF-22: compute holding_days dynamically for OPEN positions
        db_holding = _to_int(row.get("holding_days"))
        entry_d = _iso_date(row.get("entry_date"))
        if (db_holding is None or db_holding <= 0) and entry_d:
            try:
                ed = _dt_date.fromisoformat(str(entry_d))
                db_holding = max(0, (today_date - ed).days)
            except (ValueError, TypeError):
                pass
        db_holding = max(0, db_holding or 0)
        results.append({
            "position_id": row.get("position_id"),
            "report_id": row.get("report_id"),
            "stock_code": row.get("stock_code"),
            "stock_name": row.get("stock_name_snapshot") or row.get("stock_code"),
            "strategy_type": row.get("strategy_type"),
            "capital_tier": row.get("capital_tier"),
            "position_status": row.get("position_status"),
            "signal_date": _iso_date(row.get("signal_date")),
            "entry_date": entry_d,
            "actual_entry_price": _to_float(row.get("actual_entry_price")),
            "exit_date": _iso_date(row.get("exit_date")),
            "exit_price": _to_float(row.get("exit_price")),
            "shares": _to_int(row.get("shares")) or 0,
            "holding_days": db_holding,
            "net_return_pct": _to_float(row.get("net_return_pct")),
            "skip_reason": row.get("skip_reason"),
        })
    return results


def _load_equity_curve_points(
    db: Session,
    *,
    capital_tier: str,
    baseline_type: str | None = None,
) -> list[dict[str, Any]]:
    if baseline_type is None:
        rows = _execute_mappings(
            db,
            """
            SELECT trade_date, equity
            FROM sim_equity_curve_point
            WHERE capital_tier = :capital_tier
            ORDER BY trade_date ASC
            """,
            {"capital_tier": capital_tier},
        ).all()
    else:
        rows = _execute_mappings(
            db,
            """
            SELECT trade_date, equity
            FROM baseline_equity_curve_point
            WHERE capital_tier = :capital_tier
              AND baseline_type = :baseline_type
            ORDER BY trade_date ASC
            """,
            {"capital_tier": capital_tier, "baseline_type": baseline_type},
        ).all()
    return [{"date": _iso_date(row.get("trade_date")), "equity": _to_float(row.get("equity")) or 0.0} for row in rows]


def _sanitize_baseline_curve_points(points: list[dict[str, Any]]) -> list[dict[str, Any]]:
    if not points:
        return []
    if not any((_to_float(point.get("equity")) or 0.0) > 0 for point in points):
        return []
    first_positive_idx = next(
        (i for i, p in enumerate(points) if (_to_float(p.get("equity")) or 0.0) > 0),
        0,
    )
    return points[first_positive_idx:]


