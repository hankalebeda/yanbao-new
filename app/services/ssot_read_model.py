def _build_dashboard_baselines_from_snapshot_rows(
    baseline_rows: list[dict[str, Any]],
    *,
    window_days: int,
) -> tuple[dict[str, Any] | None, dict[str, Any] | None]:
    def _baseline_snapshot_ready(row: dict[str, Any]) -> bool:
        sample_size = _to_int(row.get("sample_size")) or 0
        cumulative_return_pct = _to_float(row.get("cumulative_return_pct"))
        if sample_size <= 0:
            return False
        # Latest runtime snapshots have emitted -1/-0.9999 sentinel-like values that
        # collapse baseline equity curves to zero and manufacture bogus alpha.
        if cumulative_return_pct is not None and cumulative_return_pct <= -0.9999:
            return False
        return True

    baseline_random = None
    baseline_ma_cross = None
    for row in baseline_rows:
        target = {
            "window_days": window_days,
            "sample_size": _to_int(row.get("sample_size")) or 0,
            "win_rate": _to_float(row.get("win_rate")),
            "profit_loss_ratio": _to_float(row.get("profit_loss_ratio")),
            "cumulative_return_pct": _to_float(row.get("cumulative_return_pct")),
            "display_hint": row.get("display_hint"),
        }
        if not _baseline_snapshot_ready(row):
            if row.get("baseline_type") == "baseline_random":
                baseline_random = None
            elif row.get("baseline_type") == "baseline_ma_cross":
                baseline_ma_cross = None
            continue
        if row.get("baseline_type") == "baseline_random":
            baseline_random = target
        elif row.get("baseline_type") == "baseline_ma_cross":
            baseline_ma_cross = target
    return baseline_random, baseline_ma_cross


