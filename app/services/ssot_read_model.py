def _viewer_cutoff_trade_date(db: Session, viewer_tier: str | None, viewer_role: str | None) -> str | None:
    role = (viewer_role or "").strip().lower()
    tier = (viewer_tier or "Free").strip()
    if role in {"admin", "super_admin"} or tier == "Enterprise":
        return None
    window = 90 if tier == "Pro" else 7
    latest = _viewer_cutoff_anchor_trade_date(db)
    if not latest:
        return None
    start = (date.fromisoformat(latest) - timedelta(days=540)).isoformat()
    trade_days = trade_days_in_range(start, latest)
    if not trade_days:
        return latest
    if len(trade_days) <= window:
        return trade_days[0]
    return trade_days[-window]


