def _parse_prior_stats(value: Any) -> Any:
    """Parse prior_stats_snapshot, handling the string 'null' serialization bug."""
    if value is None or value == "null":
        return None
    if isinstance(value, str):
        import json
        try:
            return json.loads(value)
        except (json.JSONDecodeError, ValueError):
            return value
    return value


