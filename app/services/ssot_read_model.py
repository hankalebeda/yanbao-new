    _today_d = _dt_date.today()
    items = []
    for row in rows:
        # BF-22: recompute invalid OPEN-position holding days from entry_date.
        hd = _to_int(row.get("holding_days"))
        if (hd is None or hd <= 0) and row.get("position_status") == "OPEN" and row.get("entry_date"):
            try:
                ed = _dt_date.fromisoformat(str(_iso_date(row.get("entry_date"))))
                hd = max(0, (_today_d - ed).days)
            except (ValueError, TypeError):
                pass
        items.append({
            "position_id": row["position_id"],
            "report_id": row["report_id"],
            "stock_code": row["stock_code"],
            "stock_name": row["stock_name_snapshot"] or row["stock_code"],
            "strategy_type": row["strategy_type"],
            "signal_date": _iso_date(row["signal_date"]),
            "sim_open_date": _iso_date(row.get("entry_date") or row.get("signal_date")),
            "sim_open_price": _to_float(row.get("signal_entry_price")) or 0,
            "actual_entry_price": _to_float(row.get("actual_entry_price")),
            "sim_qty": _to_int(row.get("shares")),
            "capital_tier": compat_capital_tier(row.get("capital_tier")),
            "capital_tier_raw": row.get("capital_tier"),
            "stop_loss_price": _to_float(row.get("stop_loss_price")) or 0,
            "target_price_1": _to_float(row.get("target_price")),
            "target_price_2": None,
            "valid_until": None,
            "status": compat_position_status(row.get("position_status")),
            "position_status_raw": row.get("position_status"),
            "sim_close_date": _iso_date(row.get("exit_date")),
            "sim_close_price": _to_float(row.get("exit_price")),
            "sim_pnl_gross": None,
            "sim_pnl_net": None,
            "sim_pnl_pct": (_to_float(row.get("net_return_pct")) or 0.0) * 100 if row.get("net_return_pct") is not None else None,
            "hold_days": hd,
            "execution_blocked": row.get("position_status") == "SKIPPED",
            "skip_reason": row.get("skip_reason"),
            "status_reason": row.get("status_reason"),
        })
