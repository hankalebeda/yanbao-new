def _load_dashboard_source_facts(
    db: Session,
    *,
    window_start: str,
    window_end: str,
    window_days: int,
) -> dict[str, Any]:
    summary_row = _execute_mappings(
        db,
        """
        SELECT
            COUNT(*) AS total_reports,
            AVG(confidence) AS avg_confidence,
            MAX(CASE WHEN quality_flag IN ('degraded', 'failed', 'llm_degraded', 'rule_fallback') THEN 1 ELSE 0 END) AS has_degraded_report
        FROM report
        WHERE is_deleted = 0
          AND published = 1
          AND trade_date BETWEEN :window_start AND :window_end
        """,
        {"window_start": window_start, "window_end": window_end},
    ).first()
    settled_rows = _execute_mappings(
        db,
        """
        SELECT DISTINCT
            s.report_id,
            COALESCE(r.strategy_type, s.strategy_type) AS strategy_type,
            s.net_return_pct
        FROM settlement_result s
        JOIN report r ON r.report_id = s.report_id
        WHERE s.signal_date BETWEEN :window_start AND :window_end
          AND s.window_days = :window_days
          AND s.settlement_status = 'settled'
          AND r.is_deleted = 0
          AND r.published = 1
        """,
        {"window_start": window_start, "window_end": window_end, "window_days": window_days},
    ).all()
    settled_count_by_strategy_ids = {key: set() for key in _DASHBOARD_STRATEGIES}
    for row in settled_rows:
        strategy_key = str(row.get("strategy_type") or "")
        report_id = str(row.get("report_id") or "")
        if strategy_key in settled_count_by_strategy_ids and report_id:
            settled_count_by_strategy_ids[strategy_key].add(report_id)
    settled_count_by_strategy = {
        key: len(report_ids)
        for key, report_ids in settled_count_by_strategy_ids.items()
    }
    settled_count = sum(settled_count_by_strategy.values())
    buy_report_rows = _execute_mappings(
        db,
        """
        SELECT strategy_type, COUNT(*) AS total_buy_reports
        FROM report
        WHERE is_deleted = 0
          AND published = 1
          AND recommendation = 'BUY'
          AND trade_date BETWEEN :window_start AND :window_end
        GROUP BY strategy_type
        """,
        {"window_start": window_start, "window_end": window_end},
    ).all()
    buy_report_count_by_strategy = {
        str(row.get("strategy_type") or ""): _to_int(row.get("total_buy_reports")) or 0
        for row in buy_report_rows
    }
    return {
        "summary_row": summary_row,
        "settled_rows": settled_rows,
        "settled_count": settled_count,
        "settled_count_by_strategy": settled_count_by_strategy,
        "buy_report_count_by_strategy": buy_report_count_by_strategy,
    }


