def _execute_mappings(
    db: Session,
    sql_text: str,
    params: dict[str, Any] | None = None,
    *,
    expanding: tuple[str, ...] = (),
):
    stmt = text(sql_text)
    for key in expanding:
        stmt = stmt.bindparams(bindparam(key, expanding=True))
    return db.execute(stmt, params or {}).mappings()


def _scalar(db: Session, sql_text: str, params: dict[str, Any] | None = None) -> Any:
    return db.execute(text(sql_text), params or {}).scalar()


