def _has_complete_public_batch_trace(db: Session, *, trade_date: str) -> bool:
    from app.services.stock_pool import get_exact_pool_view

    if get_exact_pool_view(db, trade_date=trade_date) is None:
        return False
    pool_row = _execute_mappings(
        db,
        """
        SELECT status, core_pool_size
        FROM stock_pool_refresh_task
        WHERE trade_date = :trade_date
          AND status IN ('COMPLETED', 'FALLBACK')
        ORDER BY
          CASE WHEN status = 'COMPLETED' THEN 0 ELSE 1 END,
          updated_at DESC,
          finished_at DESC,
          created_at DESC
        LIMIT 1
        """,
        {"trade_date": trade_date},
    ).first()
    task_row = _execute_mappings(
        db,
        """
        SELECT
            COUNT(*) AS total_tasks,
            SUM(CASE WHEN status IN ('Completed', 'Failed', 'Expired') THEN 1 ELSE 0 END) AS terminal_tasks,
            SUM(CASE WHEN status = 'Completed' THEN 1 ELSE 0 END) AS completed_tasks,
            SUM(CASE WHEN status IN ('Pending', 'Processing', 'Suspended') THEN 1 ELSE 0 END) AS nonterminal_tasks
        FROM report_generation_task
        WHERE trade_date = :trade_date
        """,
        {"trade_date": trade_date},
    ).first()
    published_report_count = _to_int(
        _scalar(
            db,
            """
            SELECT COUNT(*)
            FROM report
            WHERE trade_date = :trade_date
              AND published = 1
              AND is_deleted = 0
            """,
            {"trade_date": trade_date},
        )
    ) or 0
    total_tasks = _to_int(task_row.get("total_tasks")) or 0 if task_row else 0
    terminal_tasks = _to_int(task_row.get("terminal_tasks")) or 0 if task_row else 0
    completed_tasks = _to_int(task_row.get("completed_tasks")) or 0 if task_row else 0
    nonterminal_tasks = _to_int(task_row.get("nonterminal_tasks")) or 0 if task_row else 0
    expected_pool_size = _to_int(pool_row.get("core_pool_size")) if pool_row else None
    if (
        expected_pool_size is None
        or expected_pool_size <= 0
        or total_tasks <= 0
        or completed_tasks <= 0
        or nonterminal_tasks > 0
        or published_report_count <= 0
    ):
        return False
    return (
        completed_tasks >= expected_pool_size
        and terminal_tasks >= expected_pool_size
        and published_report_count >= expected_pool_size
    )


def _has_complete_stats_snapshot_set(db: Session, *, trade_date: str) -> bool:
    for current_window in _DASHBOARD_WINDOWS:
        window_start, window_end = _dashboard_window_bounds(date.fromisoformat(trade_date), current_window)
        facts = _load_dashboard_source_facts(
            db,
            window_start=window_start,
            window_end=window_end,
            window_days=current_window,
        )
        strategy_rows = _load_dashboard_strategy_snapshot_rows(
            db,
            snapshot_date=trade_date,
            window_days=current_window,
        )
        baseline_rows = _load_dashboard_baseline_snapshot_rows(
            db,
            snapshot_date=trade_date,
            window_days=current_window,
        )
        strategy_keys = {str(row.get("strategy_type") or "") for row in strategy_rows}
        baseline_keys = {str(row.get("baseline_type") or "") for row in baseline_rows}
        if strategy_keys != set(_DASHBOARD_STRATEGIES) or baseline_keys != set(_DASHBOARD_BASELINES):
            return False
        by_strategy_type = _build_dashboard_strategy_metrics_from_snapshot_rows(strategy_rows)
        total_sample = sum(by_strategy_type[key]["sample_size"] for key in _DASHBOARD_STRATEGIES)
        if total_sample != facts["settled_count"]:
            return False
        if any(
            by_strategy_type[key]["sample_size"] != facts["settled_count_by_strategy"].get(key, 0)
            for key in _DASHBOARD_STRATEGIES
        ):
            return False
        if any(
            str(row.get("data_status") or "").upper() != "READY"
            for row in strategy_rows
            if (_to_int(row.get("sample_size")) or 0) > 0
        ):
            return False
    return True


