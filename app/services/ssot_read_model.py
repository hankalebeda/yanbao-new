def _sim_runtime_source_state(db: Session, *, capital_tier: str) -> tuple[int, int]:
    closed_position_count = _to_int(
        _scalar(
            db,
            """
            SELECT COUNT(*)
            FROM sim_position
            WHERE capital_tier = :capital_tier
              AND position_status IN ('TAKE_PROFIT', 'STOP_LOSS', 'TIMEOUT', 'DELISTED_LIQUIDATED')
            """,
            {"capital_tier": capital_tier},
        )
    ) or 0
    open_position_count = _to_int(
        _scalar(
            db,
            """
            SELECT COUNT(*)
            FROM sim_position
            WHERE capital_tier = :capital_tier
              AND position_status = 'OPEN'
            """,
            {"capital_tier": capital_tier},
        )
    ) or 0
    return closed_position_count, open_position_count


def _clean_conclusion(raw: Any, fallback: str) -> str:
    """Filter out mock LLM text, return meaningful fallback."""
    text = str(raw or "").strip()
    if not text or "generated by mock" in text.lower():
        return fallback
    return text


def _truncate_text(value: Any, limit: int = 200) -> tuple[str, bool]:
    text = str(value or "").strip()
    if len(text) <= limit:
        return text, False
    return f"{text[: max(0, limit - 3)]}...", True


def _to_float(value: Any) -> float | None:
    if value is None:
        return None
    if isinstance(value, Decimal):
        return float(value)
    if isinstance(value, bool):
        return float(int(value))
    if isinstance(value, (int, float)):
        return float(value)
    try:
        return float(value)
    except (TypeError, ValueError):
        return None


def _sanitize_dashboard_alpha_annual(value: Any) -> float | None:
    alpha_annual = _to_float(value)
    if alpha_annual is None or not math.isfinite(alpha_annual):
        return None
    # alpha_annual is expressed as a decimal ratio (0.1 = 10%). Values in the
    # multi-hundred-percent range can happen, but orders-of-magnitude spikes are
    # broken snapshots and should fail closed in public dashboards.
    if abs(alpha_annual) > _MAX_REASONABLE_DASHBOARD_ALPHA_ANNUAL:
        return None
    return alpha_annual


def _format_money_cn(value: Any) -> str | None:
    numeric_value = _to_float(value)
    if numeric_value is None:
        return None
    sign = "-" if numeric_value < 0 else ""
    amount = abs(numeric_value)
    if amount >= 1e12:
        return f"{sign}{amount / 1e12:.2f} 万亿"
    if amount >= 1e8:
        return f"{sign}{amount / 1e8:.2f} 亿"
    if amount >= 1e4:
        return f"{sign}{amount / 1e4:.2f} 万"
    return f"{sign}{amount:.2f}"


