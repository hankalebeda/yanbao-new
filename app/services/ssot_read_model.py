def _load_latest_position_map_ssot(
    db: Session,
    report_ids: list[str],
    *,
    capital_tier: str = "100k",
) -> dict[str, str | None]:
    if not report_ids:
        return {}
    rows = _execute_mappings(
        db,
        """
        SELECT report_id, position_status, updated_at, created_at
        FROM sim_position
        WHERE capital_tier = :capital_tier
          AND report_id IN :report_ids
        ORDER BY report_id ASC, COALESCE(updated_at, created_at) DESC, created_at DESC
        """,
        {"capital_tier": capital_tier, "report_ids": report_ids},
        expanding=("report_ids",),
    ).all()
