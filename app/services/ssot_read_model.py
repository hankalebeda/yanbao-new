def _looks_like_internal_conclusion(conclusion: str | None) -> bool:
    text = str(conclusion or "").strip()
    if not text:
        return False
    lowered = text.lower()
    return any(marker in text for marker in _INTERNAL_CONCLUSION_MARKERS) or "generated by mock" in lowered


def _looks_like_internal_reasoning(reasoning: str | None) -> bool:
    text = str(reasoning or "").strip()
    if not text:
        return False
    lowered = text.lower()
    return any(marker in text for marker in _INTERNAL_REASONING_MARKERS) or any(
        marker in lowered for marker in ("generated by mock", "fallback=rule_based")
    )


def _build_user_fallback_conclusion(report: Mapping[str, Any], *, stock_name: str | None = None) -> str:
    name = stock_name or str(report.get("stock_name_snapshot") or report.get("stock_code") or "该股票")
    recommendation = _humanize_recommendation(report.get("recommendation"))
    market_state = _humanize_market_state(report.get("market_state"))
    confidence = _to_float(report.get("confidence"))
    strategy_type = str(report.get("strategy_type") or "B")
    quality_flag = str(report.get("quality_flag") or "ok")

    parts = [f"{name}当前处于{market_state}市场环境，策略判断为{recommendation}"]
    if confidence is not None:
        parts.append(f"当前信号置信度约为{confidence * 100:.0f}%")
    parts.append(f"策略类型为{strategy_type}")
    if quality_flag != "ok":
        parts.append("且部分数据处于降级状态")
    return "，".join(parts) + "。当前结论由规则引擎生成，请结合操作信号卡与风险承受能力审慎参考。"


def _build_user_fallback_reasoning(report: Mapping[str, Any]) -> str:
    name = str(report.get("stock_name_snapshot") or report.get("stock_code") or "该股票")
    recommendation = _humanize_recommendation(report.get("recommendation"))
    market_state = _humanize_market_state(report.get("market_state"))
    confidence = _to_float(report.get("confidence"))
    strategy_type = str(report.get("strategy_type") or "B")
    quality_flag = str(report.get("quality_flag") or "ok")

    lines = [
        f"{name}推理摘要",
        "基础信号整理",
        f"- 市场环境：{market_state}",
        f"- 策略类型：{strategy_type}",
        f"- 当前判断：{recommendation}",
    ]
    if confidence is not None:
        lines.append(f"- 置信度：{confidence * 100:.0f}%")
    lines.append(f"- 数据状态：{'正常' if quality_flag == 'ok' else f'已降级（{quality_flag}）'}")
    lines.append("- 说明：当前报告由规则引擎生成，完整模型推理链暂未产出。")
    lines.append("- 建议：请结合上方结论、操作信号卡和自身风险承受能力综合判断。")
    return "\n".join(lines)


def _sanitize_conclusion_text(
    conclusion: str | None,
    *,
    report: Mapping[str, Any] | None = None,
    stock_name: str | None = None,
) -> str | None:
    """Normalize internal fallback conclusion text into a user-facing summary."""
    if not conclusion:
        return conclusion
    if _looks_like_internal_conclusion(conclusion):
        return _build_user_fallback_conclusion(report or {}, stock_name=stock_name)
    return conclusion


def _sanitize_reasoning_text(reasoning: str | None, *, report: Mapping[str, Any]) -> str:
    if not reasoning:
        return ""
    if str(report.get("llm_fallback_level") or "").lower() == "failed" or _looks_like_internal_reasoning(reasoning):
        return _build_user_fallback_reasoning(report)
    return str(reasoning)


def _load_ssot_report_bundle(
    db: Session,
    *,
    report_id: str | None = None,
    stock_code: str | None = None,
    viewer_tier: str | None = None,
    viewer_role: str | None = None,
) -> dict[str, Any] | None:
    if report_storage_mode(db) != "ssot":
        return None
    if not report_id and not stock_code:
        return None

    where = ["r.is_deleted = 0", "r.published = 1", "COALESCE(LOWER(r.quality_flag), 'ok') = 'ok'"]
    params: dict[str, Any] = {}
    if report_id:
        where.append("r.report_id = :report_id")
        params["report_id"] = report_id
    if stock_code:
        where.append("r.stock_code = :stock_code")
        params["stock_code"] = stock_code
    cutoff = _viewer_cutoff_trade_date(db, viewer_tier, viewer_role)
    if cutoff:
        where.append("r.trade_date >= :viewer_cutoff")
        params["viewer_cutoff"] = cutoff

    row = (
        _execute_mappings(
            db,
            f"""
            SELECT
                r.report_id,
                r.trade_date,
                r.stock_code,
                r.stock_name_snapshot,
                r.published,
                r.publish_status,
                r.recommendation,
                r.confidence,
                r.quality_flag,
                r.status_reason,
                r.llm_fallback_level,
                r.strategy_type,
                r.market_state,
                r.conclusion_text,
                r.reasoning_chain_md,
                r.content_json,
                r.llm_actual_model,
                r.llm_provider_name,
                r.llm_endpoint,
                r.prior_stats_snapshot,
                r.risk_audit_status,
                r.risk_audit_skip_reason,
                r.review_flag,
                r.failure_category,
                r.negative_feedback_count,
                r.created_at,
                r.updated_at,
                sm.industry AS stock_industry
            FROM report r
            LEFT JOIN stock_master sm ON sm.stock_code = r.stock_code
            WHERE {" AND ".join(where)}
            ORDER BY r.trade_date DESC, r.created_at DESC
            LIMIT 1
            """,
            params,
        )
        .first()
    )
    if not row:
        return None

    report_id = str(row["report_id"])
    citations = _execute_mappings(
        db,
        """
        SELECT citation_id, citation_order, source_name, source_url, fetch_time, title, excerpt, created_at
        FROM report_citation
        WHERE report_id = :report_id
        ORDER BY citation_order ASC, created_at ASC
        """,
        {"report_id": report_id},
    ).all()
    instruction_card = _execute_mappings(
        db,
        """
        SELECT signal_entry_price, atr_pct, atr_multiplier, stop_loss, target_price, stop_loss_calc_mode, created_at
        FROM instruction_card
        WHERE report_id = :report_id
        LIMIT 1
        """,
        {"report_id": report_id},
    ).first()
    sim_trade_instructions = _execute_mappings(
        db,
        """
        SELECT capital_tier, status, position_ratio, skip_reason, created_at
        FROM sim_trade_instruction
        WHERE report_id = :report_id
        ORDER BY capital_tier ASC
        """,
        {"report_id": report_id},
    ).all()
    used_data = _execute_mappings(
        db,
        """
        SELECT
            u.usage_id,
            u.trade_date,
            u.stock_code,
            u.dataset_name,
            u.source_name,
            u.batch_id,
            u.fetch_time,
            u.status,
            u.status_reason,
            l.report_data_usage_link_id,
            l.created_at AS linked_at
        FROM report_data_usage_link l
        JOIN report_data_usage u ON u.usage_id = l.usage_id
        WHERE l.report_id = :report_id
        ORDER BY u.trade_date DESC, u.source_name ASC, u.dataset_name ASC
        """,
        {"report_id": report_id},
    ).all()
    # v25 front-end parity: load stock_master profile + latest kline snapshot on/before trade_date
    stock_master_row = _execute_mappings(
        db,
        """
        SELECT stock_code, stock_name, exchange, industry, list_date, circulating_shares,
               is_st, is_suspended, is_delisted
        FROM stock_master
        WHERE stock_code = :stock_code
        LIMIT 1
        """,
        {"stock_code": row["stock_code"]},
    ).first()
    kline_row = _execute_mappings(
        db,
        """
        WITH preferred_adjust AS (
            SELECT CASE
                WHEN EXISTS(
                    SELECT 1
                    FROM kline_daily
                    WHERE stock_code = :stock_code
                      AND trade_date <= :trade_date
                      AND LOWER(COALESCE(adjust_type, '')) = 'front_adjusted'
                ) THEN 'front_adjusted'
                ELSE (
                    SELECT LOWER(COALESCE(adjust_type, ''))
                    FROM kline_daily
                    WHERE stock_code = :stock_code
                      AND trade_date <= :trade_date
                    ORDER BY trade_date DESC, created_at DESC, kline_id DESC
                    LIMIT 1
                )
            END AS adjust_type
        ),
        ranked_kline AS (
            SELECT
                trade_date,
                open,
                high,
                low,
                close,
                volume,
                amount,
                atr_pct,
                turnover_rate,
                ma5,
                ma10,
                ma20,
                ma60,
                volatility_20d,
                hs300_return_20d,
                is_suspended,
                ROW_NUMBER() OVER (
                    PARTITION BY trade_date
                    ORDER BY created_at DESC, kline_id DESC
                ) AS row_num
            FROM kline_daily
            WHERE stock_code = :stock_code
              AND trade_date <= :trade_date
              AND LOWER(COALESCE(adjust_type, '')) = (SELECT adjust_type FROM preferred_adjust)
        )
        SELECT trade_date, open, high, low, close, volume, amount, atr_pct, turnover_rate,
               ma5, ma10, ma20, ma60, volatility_20d, hs300_return_20d, is_suspended
        FROM ranked_kline
        WHERE row_num = 1
        ORDER BY trade_date DESC
        LIMIT 2
        """,
        {"stock_code": row["stock_code"], "trade_date": row["trade_date"]},
    ).all()
    return {
        "report": dict(row),
        "citations": [dict(item) for item in citations],
        "instruction_card": dict(instruction_card) if instruction_card else None,
        "sim_trade_instructions": [dict(item) for item in sim_trade_instructions],
        "used_data": [dict(item) for item in used_data],
        "stock_master": dict(stock_master_row) if stock_master_row else None,
        "kline_snapshot": [dict(item) for item in kline_row],
    }


