def _can_view_full(viewer_tier: str | None, viewer_role: str | None) -> bool:
    role = (viewer_role or "").strip().lower()
    tier = (viewer_tier or "").strip()
    return role in {"admin", "super_admin"} or tier in {"Pro", "Enterprise"}


def _masked_price_string(value: Any, *, can_see_full: bool) -> str | None:
    numeric_value = _to_float(value)
    if numeric_value is None:
        return None
    if not can_see_full:
        return "¥**.**"
    return f"{numeric_value:.2f}"


def _normalize_atr_ratio(value: Any) -> float | None:
    numeric_value = _to_float(value)
    if numeric_value is None or numeric_value <= 0:
        return None
    return numeric_value / 100.0 if numeric_value > 1 else numeric_value


def _build_term_context(
    report: dict[str, Any],
    instruction_card: dict[str, Any] | None,
    *,
    can_see_full: bool,
) -> dict[str, str]:
    atr_ratio = _normalize_atr_ratio((instruction_card or {}).get("atr_pct"))
    atr_multiplier = _to_float((instruction_card or {}).get("atr_multiplier"))
    confidence = _to_float(report.get("confidence"))
    recommendation_label = {
        "BUY": "买入",
        "SELL": "卖出",
        "HOLD": "观望等待",
    }
    quality_label = {
        "ok": "数据正常",
        "stale_ok": "数据延迟（已回退至可用批次）",
        "degraded": "数据降级展示",
        "failed": "数据不可用",
    }
    audit_label = {
        "completed": "已完成",
        "skipped": "已跳过",
        "not_triggered": "尚未触发",
    }
    strategy_label = {
        "A": "事件驱动",
        "B": "趋势跟踪",
        "C": "低波套利",
    }
    market_state_label = {
        "BULL": "偏强",
        "NEUTRAL": "震荡",
        "BEAR": "偏弱",
    }
    context = {
        "recommendation": recommendation_label.get(str(report.get("recommendation") or ""), "观望等待"),
        "quality_flag": quality_label.get(str(report.get("quality_flag") or ""), "数据处理中"),
        "risk_audit_status": audit_label.get(str(report.get("risk_audit_status") or ""), "处理中"),
        "strategy_type": strategy_label.get(str(report.get("strategy_type") or ""), "策略待确认"),
        "market_state": market_state_label.get(str(report.get("market_state") or ""), "震荡"),
    }
    if confidence is not None:
        context["confidence"] = f"{confidence:.2f}"
    if atr_ratio is not None:
        atr_pct_text = f"{atr_ratio * 100:.1f}%"
        atr_multiplier_text = f"{atr_multiplier:.1f}" if atr_multiplier is not None else "1.0"
        context["ATR"] = (
            f"ATR={atr_pct_text}，代表该股近期平均每日波动约 {atr_pct_text}，"
            f"当前止损按 {atr_multiplier_text} 倍 ATR 计算。"
        )
        context["atr_pct"] = atr_pct_text
    for key in ("signal_entry_price", "stop_loss", "target_price"):
        masked_value = _masked_price_string((instruction_card or {}).get(key), can_see_full=can_see_full)
        if masked_value is not None:
            context[key] = masked_value

    # OPT-05: 补充 MA5/MA20 场景化解释
    _raw_cj = report.get("content_json")
    if isinstance(_raw_cj, str):
        try:
            content_json = json.loads(_raw_cj)
        except (json.JSONDecodeError, TypeError):
            content_json = {}
    elif isinstance(_raw_cj, dict):
        content_json = _raw_cj
    else:
        content_json = {}
    kline_snap = content_json.get("kline_snapshot") or {}
    try:
        _cl = _to_float(kline_snap.get("close"))
        _m5 = _to_float(kline_snap.get("ma5"))
        _m20 = _to_float(kline_snap.get("ma20"))
        if _cl and _m5 and _m20:
            if _cl > _m5 > _m20:
                context["ma_structure"] = f"MA结构：多头排列（收盘{_cl:.2f} > MA5={_m5:.2f} > MA20={_m20:.2f}），价格趋势偏强。"
            elif _cl < _m5 < _m20:
                context["ma_structure"] = f"MA结构：空头排列（收盘{_cl:.2f} < MA5={_m5:.2f} < MA20={_m20:.2f}），价格趋势偏弱。"
            elif _cl > _m20:
                context["ma_structure"] = f"MA结构：价格站于MA20上方（收盘{_cl:.2f}, MA20={_m20:.2f}），中期偏多。"
            else:
                context["ma_structure"] = f"MA结构：价格跌破MA20（收盘{_cl:.2f}, MA20={_m20:.2f}），注意中期支撑失守风险。"
    except (TypeError, ValueError):
        pass

    # OPT-05: 补充 PE/PB 估值情境解释
    profile_snap = content_json.get("stock_profile_snapshot") or {}
    try:
        _pe = _to_float(profile_snap.get("pe_ttm"))
        _pb = _to_float(profile_snap.get("pb"))
        if _pe and _pe > 0:
            if _pe < 15:
                pe_label = "低估（PE<15，历史低位）"
            elif _pe < 30:
                pe_label = "适中（PE 15~30）"
            elif _pe < 50:
                pe_label = "偏高（PE 30~50，需关注业绩兑现）"
            else:
                pe_label = "高估（PE>50，泡沫风险偏大）"
            pb_desc = f"，PB={_pb:.2f}" if _pb and _pb > 0 else ""
            context["valuation"] = f"估值：PE(TTM)={_pe:.1f}{pb_desc}，{pe_label}。"
    except (TypeError, ValueError):
        pass

    return context


