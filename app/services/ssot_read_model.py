def _default_strategy_metric() -> dict[str, Any]:
    return {
        "sample_size": 0,
        "coverage_pct": 0.0,
        "win_rate": None,
        "profit_loss_ratio": None,
        "alpha_annual": None,
        "max_drawdown_pct": None,
        "cumulative_return_pct": None,
        "signal_validity_warning": False,
        "display_hint": None,
    }


def _coverage_pct(sample_size: int, denominator: int) -> float:
    if sample_size <= 0 or denominator <= 0:
        return 0.0
    return round(sample_size / denominator, 6)


def _weighted_metric(rows: list[dict[str, Any]], field_name: str) -> float | None:
    weighted_sum = 0.0
    total_weight = 0
    for row in rows:
        field_value = _to_float(row.get(field_name))
        weight = _to_int(row.get("sample_size")) or 0
        if field_value is None or weight <= 0:
            continue
        weighted_sum += field_value * weight
        total_weight += weight
    if total_weight <= 0:
        return None
    return round(weighted_sum / total_weight, 6)


def _empty_dashboard_strategy_metrics(
    *,
    settled_count_by_strategy: dict[str, int],
    buy_report_count_by_strategy: dict[str, int],
) -> dict[str, Any]:
    by_strategy_type = {key: _default_strategy_metric() for key in _DASHBOARD_STRATEGIES}
    for key in _DASHBOARD_STRATEGIES:
        sample_size = int(settled_count_by_strategy.get(key, 0) or 0)
        metric = _default_strategy_metric()
        metric["sample_size"] = sample_size
        metric["coverage_pct"] = _coverage_pct(sample_size, buy_report_count_by_strategy.get(key, 0) or 0)
        by_strategy_type[key] = metric
    return by_strategy_type


