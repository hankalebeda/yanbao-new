import json
with open("app/governance/feature_registry.json", "r", encoding="utf-8") as f:
    reg = json.load(f)
total = len(reg["features"])
audited = sum(1 for feat in reg["features"] if feat.get("spec_requirement"))
has_gaps = sum(1 for feat in reg["features"] if feat.get("gaps"))
total_gaps = sum(len(feat.get("gaps", [])) for feat in reg["features"])
code_ok = sum(1 for feat in reg["features"] if "\u2705" in feat.get("code_verdict", ""))
code_warn = sum(1 for feat in reg["features"] if "\u26a0\ufe0f" in feat.get("code_verdict", ""))
code_red = sum(1 for feat in reg["features"] if "\U0001f534" in feat.get("code_verdict", ""))
code_crit = sum(1 for feat in reg["features"] if "\u274c" in feat.get("code_verdict", ""))
test_ok = sum(1 for feat in reg["features"] if "\u2705" in feat.get("test_verdict", ""))
test_warn = sum(1 for feat in reg["features"] if "\u26a0\ufe0f" in feat.get("test_verdict", ""))
test_red = sum(1 for feat in reg["features"] if "\U0001f534" in feat.get("test_verdict", ""))
print(f"Version: {reg['version']}")
print(f"Total: {total}, Audited: {audited}")
print(f"Features with gaps: {has_gaps}, Total gap items: {total_gaps}")
print(f"Code: OK={code_ok} WARN={code_warn} RED={code_red} CRIT={code_crit}")
print(f"Test: OK={test_ok} WARN={test_warn} RED={test_red}")
